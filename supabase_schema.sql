create policy "authenticated read cities" on public.cities
for select to authenticated using (true);

create policy "admin manage cities" on public.cities
for all to authenticated using (public.is_admin_user()) with check (public.is_admin_user());

create policy "authenticated read employees" on public.employees
for select to authenticated using (true);

create policy "authenticated read sources" on public.sources
for select to authenticated using (true);

create policy "admin and call center create sources" on public.sources
for insert to authenticated with check (exists (
  select 1
  from public.employees e
  where e.auth_user_id = auth.uid()
    and e.employee_type in ('admin', 'call_center')
));

create policy "crm roles read orders" on public.orders
for select to authenticated using (
  public.can_current_user_manage_orders()
  or technician_id in (
    select e.id from public.employees e where e.auth_user_id = auth.uid()
  )
);

create policy "crm roles insert orders" on public.orders
for insert to authenticated with check (public.can_current_user_manage_orders());

create policy "crm roles update orders" on public.orders
for update to authenticated
using (
  public.can_current_user_manage_orders()
  or technician_id = public.current_employee_id()
)
with check (
  public.can_current_user_manage_orders()
  or technician_id = public.current_employee_id()
);

create policy "admin delete orders only" on public.orders
for delete to authenticated using (public.can_current_user_delete_orders());

create policy "admin read employee private" on public.employee_private
for select to authenticated using (public.is_admin_user());

create policy "admin manage employees" on public.employees
for all to authenticated using (public.is_admin_user()) with check (public.is_admin_user());

create policy "admin manage employee private" on public.employee_private
for all to authenticated using (public.is_admin_user()) with check (public.is_admin_user());

create policy "authenticated read history" on public.order_history
for select to authenticated using (true);

create policy "crm roles insert history" on public.order_history
for insert to authenticated with check (
  public.can_current_user_manage_orders()
  or exists (
    select 1
    from public.orders o
    where o.id = order_id
      and o.technician_id = public.current_employee_id()
  )
);

create policy "authenticated read day offs" on public.day_offs
for select to authenticated
using (
  public.current_employee_role() in ('admin', 'call_center')
  or exists (
    select 1
    from public.employees e
    where e.id = technician_id
      and e.id = public.current_employee_id()
  )
);

create policy "admin and call center manage day offs" on public.day_offs
for all to authenticated
using (public.current_employee_role() in ('admin', 'call_center'))
with check (public.current_employee_role() in ('admin', 'call_center'));

create policy "authenticated read busy slots" on public.busy_slots
for select to authenticated
using (
  public.current_employee_role() in ('admin', 'call_center')
  or technician_id = public.current_employee_id()
);

create policy "crm roles manage busy slots" on public.busy_slots
for all to authenticated
using (
  public.current_employee_role() in ('admin', 'call_center')
  or technician_id = public.current_employee_id()
)
with check (
  public.current_employee_role() in ('admin', 'call_center')
  or technician_id = public.current_employee_id()
);

create policy "authenticated read service catalog" on public.service_catalog
for select to authenticated using (true);

create policy "admin manage service catalog" on public.service_catalog
for all to authenticated
using (public.is_admin_user())
with check (public.is_admin_user());

